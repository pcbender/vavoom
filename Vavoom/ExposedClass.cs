    /// <summary>
    /// Class ExposedClass.
    /// </summary>
    /// <seealso cref="System.Dynamic.DynamicObject" />
    /// <autogeneratedoc />
    public class ExposedClass : DynamicObject
    {
        private readonly Type _type;
        private readonly Dictionary<string, Dictionary<int, List<MethodInfo>>> _staticMethods;
        private readonly Dictionary<string, Dictionary<int, List<MethodInfo>>> _genStaticMethods;

        private ExposedClass(Type type)
        {
            _type = type;

            _staticMethods = StaticMethods(_type);

            _genStaticMethods = GenStaticMethods(_type);

            var baseType = _type.BaseType;

            while (baseType != null)
            {
                var staticMethods = StaticMethods(baseType);

                foreach (var (key, value) in staticMethods)
                {
                    if (_staticMethods.ContainsKey(key))
                        continue;
                    _staticMethods.Add(key, value);
                }

                var genStaticMethods = GenStaticMethods(baseType);

                foreach (var (key, value) in genStaticMethods)
                {
                    if(_genStaticMethods.ContainsKey(key))
                        continue;
                    _genStaticMethods.Add(key, value);
                }

                baseType = baseType.BaseType;
            }
        }


        private Dictionary<string, Dictionary<int, List<MethodInfo>>> GenStaticMethods(Type type)
        {
            return type
                .GetMethods(BindingFlags.NonPublic | BindingFlags.Public | BindingFlags.Static)
                .Where(m => m.IsGenericMethod)
                .GroupBy(m => m.Name)
                .ToDictionary(
                    p => p.Key,
                    p => p.GroupBy(r => r.GetParameters().Length).ToDictionary(r => r.Key, r => r.ToList()));
        }

        private Dictionary<string, Dictionary<int, List<MethodInfo>>> StaticMethods(Type type)
        {
            return type
                .GetMethods(BindingFlags.NonPublic | BindingFlags.Public | BindingFlags.Static)
                .Where(m => !m.IsGenericMethod)
                .GroupBy(m => m.Name)
                .ToDictionary(
                    p => p.Key,
                    p => p.GroupBy(r => r.GetParameters().Length).ToDictionary(r => r.Key, r => r.ToList()));
        }

